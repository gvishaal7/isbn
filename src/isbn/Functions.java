    /*
     *  a function to return the length of each segment of the given isbn number
     */
    public ArrayList<Integer> hyphensegments(String isbn) {
        ArrayList<Integer> myList = new ArrayList();
        int grp = Integer.parseInt(treeGrp.search(isbn));
        int reg = Integer.parseInt(treeReg.search(isbn));
        if(grp != 0 && reg != 0) {
            myList.add(3);
            myList.add(grp);
            myList.add(reg);
            myList.add((9-grp-reg));
            myList.add(1);
        }
        return myList;
    }
    
    /*
     *  a function to hyphenate the isbn number using the lengths returned 
     *  from the two range variables.
     */
    public String hyphenformat(String isbn) {
        String pos = "";
        int start = 0;
        ArrayList<Integer> hyphenSegments = hyphensegments(isbn);
        for (int i : hyphenSegments) {
            pos += isbn.substring(start, start+i); 
            pos += "-";
            start += i;
        }
        if(start == 0) {
            return "0";
        }
        else {
            return pos.substring(0, pos.length()-1);
        }
    }
